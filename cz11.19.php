<h5>DRUGIE CZYTANIE (1 Tes 5, 1-6)</h5>
<p><i>Dzień Pański nadejdzie niespodziewanie</i></p></br>
<p>Czytanie z Pierwszego Listu św. Pawła Apostoła do Tesaloniczan.</p>
<p>Nie potrzeba wam, bracia, pisać o czasach i chwilach. Sami bowiem dokładnie wiecie, że dzień Pański przyjdzie jak złodziej w nocy.</p>
<p>Kiedy bowiem będą mówić: « Pokój i bezpieczeństwo », tak niespodziana przyjdzie na nich zagłada, jak bóle na brzemienną, i nie ujdą. Ale wy, bracia, nie jesteście w ciemnościach, aby ów dzień miał was zaskoczyć jak złodziej. Wszyscy wy bowiem jesteście synami światłości i synami dnia. Nie jesteście synami nocy ani ciemności.</p>
<p>Nie śpijmy przeto jak inni, ale czuwajmy i bądźmy trzeźwi.</p>
<p>Oto słowo Boże.</p></br>
