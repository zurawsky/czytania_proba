<h5>EWANGELIA DŁUŻSZA (Mt 25, 14-30)</h5>
<p><i>Przypowieść o talentach</i></p></br>
<p>Słowa Ewangelii według świętego Mateusza.</p>
<p>Jezus opowiedział swoim uczniom tę przypowieść:</p>
<p>« Pewien człowiek, mając się udać w podróż, przywołał swoje sługi i przekazał im swój majątek. Jednemu dał pięć talentów, drugiemu dwa, trzeciemu jeden, każdemu według jego zdolności, i odjechał. Zaraz ten, który otrzymał pięć talentów, poszedł, puścił je w obrót i zyskał drugie pięć. Tak samo i ten, który dwa otrzymał; on również zyskał drugie dwa. Ten zaś, który otrzymał jeden, poszedł i rozkopawszy ziemię, ukrył pieniądze swego pana.</p>
<p>Po dłuższym czasie powrócił pan owych sług i zaczął rozliczać się z nimi.</p>
<p>Wówczas przyszedł ten, który otrzymał pięć talentów. Przyniósł drugie pięć i rzekł: “ Panie, przekazałeś mi pięć talentów, oto drugie pięć talentów zyskałem ”. Rzekł mu pan: “ Dobrze, sługo dobry i wierny. Byłeś wierny w niewielu rzeczach, nad wieloma cię postawię: wejdź do radości twego pana ”.</p>
<p>Przyszedł również i ten, który otrzymał dwa talenty, mówiąc: “ Panie, przekazałeś mi dwa talenty, oto drugie dwa talenty zyskałem ”. Rzekł mu pan: “ Dobrze, sługo dobry i wierny. Byłeś wierny w niewielu rzeczach, nad wieloma cię postawię; wejdź do radości twego pana ”.</p>
<p>Przyszedł i ten, który otrzymał jeden talent, i rzekł: “ Panie, wiedziałem, żeś jest człowiek twardy: chcesz żąć tam, gdzie nie posiałeś, i zbierać tam, gdzieś nie rozsypał. Bojąc się więc, poszedłem i ukryłem twój talent w ziemi. Oto masz swoją własność ”.</p>
<p>Odrzekł mu pan jego: “ Sługo zły i gnuśny! Wiedziałeś, że chcę żąć tam, gdzie nie posiałem, i zbierać tam, gdziem nie rozsypał. Powinieneś więc był oddać moje pieniądze bankierom, a ja po powrocie byłbym z zyskiem odebrał swoją własność. Dlatego odbierzcie mu ten talent, a dajcie temu, który ma dziesięć talentów. Każdemu bowiem, kto ma, będzie dodane, tak że nadmiar mieć będzie. Temu zaś, kto nie ma, zabiorą nawet to, co ma. A sługę nieużytecznego wyrzućcie na zewnątrz w ciemności; tam będzie płacz i zgrzytanie zębów ” ».</p>
<p>Oto słowo Pańskie.</p></br>
